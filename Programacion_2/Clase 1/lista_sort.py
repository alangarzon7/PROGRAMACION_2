"""Si queremos ordenar una lista, podemos utilizar el método sort.
Este método solo funciona en el caso en que tengamos listas de un mismo.
Podemos ordenar tanto de orden ascendente como descendente."""
#ascendente
lista1=[10,15,2,4,1]
lista1.sort()
print(lista1)

#descendente
lista1.sort(reverse=True)
print(lista1)