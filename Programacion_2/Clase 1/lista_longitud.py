"""Cómo obtener la longitud de la lista
Para poder obtener el largo de una lista,
usamos la función len, la misma puede ser utilizada
para obtener la longitud de cualquier objeto en Python."""
lista1 =[1,2,3,4,5]
#guardo la longitud en una variable para luego llamarla en un format
longitud=len(lista1)

print(f"La longitud de tu lista es de  {longitud} elementos")